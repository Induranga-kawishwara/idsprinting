class Cashin {
  constructor(
    reasonName,
    profitOrOther,
    reasonDetails,
    amount,
    addedDateAndTime,
    addedBy
  ) {
    this.reasonName = reasonName;
    this.profitOrOther = profitOrOther;
    this.reasonDetails = reasonDetails;
    this.amount = amount;
    this.addedDateAndTime = addedDateAndTime;
    this.addedBy = addedBy;
  }
}

export default Cashin;
