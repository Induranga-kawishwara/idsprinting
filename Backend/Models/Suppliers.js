class Suppliers {
  constructor(
    name,
    email,
    contactNumber,
    address1,
    address2,
    city,
    postalCode,
    businessId,
    additionalData
  ) {
    this.name = name;
    this.email = email;
    this.contactNumber = contactNumber;
    this.address1 = address1;
    this.address2 = address2;
    this.city = city;
    this.postalcode = postalCode;
    this.businessId = businessId;
    this.additionalData = additionalData;
  }
}

export default Suppliers;
